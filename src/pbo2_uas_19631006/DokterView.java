    private void tambah_data() {
        try{
            con = DriverManager.getConnection(url, "root", "");
            pst = con.prepareStatement("insert into dokter values (?, ?, ?)");
            pst.setInt(1, 0);
            pst.setString(2, textNama.getText());
            pst.setString(3, textSpesialis.getText());
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data dokter berhasil ditambahkan!");
            ulang();
            tampil_data();
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }

    private void ubah_data() {
      try {
            con = DriverManager.getConnection(url, "root", "");
            pst = con.prepareStatement("update dokter set nama_dokter=?, spesialis=? where id_dokter=?");
            pst.setString(1, textNama.getText());
            pst.setString(2, textSpesialis.getText());
            pst.setInt(6, Integer.parseInt(labelID.getText()));
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data mahasiswa berhasil diperbaharui!");
            ulang();
            tampil_data();
        } catch (Exception e) {
            System.out.println(e.getMessage());
        } 
    }
