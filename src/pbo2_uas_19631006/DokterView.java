    private void tampil_data(){
        try {
           String[] judul = {"ID","Nama Dokter","Spesialis"};
           dtm = new DefaultTableModel(null,judul);
           tabelDokter.setModel(dtm);
           con = DriverManager.getConnection(url,"root", "");//koneksi ke database
           String sql = "";
           if(textCari.getText().isEmpty()){
               sql = "select * from dokter";
           }else{
               sql = "select * from dokter where nama_dokter like '%" + textCari.getText()+"%'";
           }
           pst = con.prepareStatement(sql);//menjalankan perintah sql
           rs = pst.executeQuery();//karena 44 query select maka tampil datanya rs(resultset)
           while(rs.next()){
               String[] data = {rs.getString(1), rs.getString(2), rs.getString(3)};
               dtm.addRow(data);
           }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }
